package PhuongTrinhBacHaiGiaoDien.modelBac2;

public class modelBac2 {
	double a, b, c, delta;

	public modelBac2(double a, double b, double c, double delta) {
		this.a = a;
		this.b = b;
		this.c = c;
		this.delta = delta;
	}

	public double getA() {
		return a;
	}

	public void setA(double a) {
		this.a = a;
	}

	public double getB() {
		return b;
	}

	public void setB(double b) {
		this.b = b;
	}

	public double getC() {
		return c;
	}

	public void setC(double c) {
		this.c = c;
	}

	public double getDelta() {
		return delta;
	}

	public void setDelta(double delta) {
		this.delta = delta;
	}

}
