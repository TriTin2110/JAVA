package GiaoDien.KeoThaGiaoDien.GhiChu;

class GhiChuModel {
	String noiDung, duongDan;

	public GhiChuModel() {
		this.noiDung = "";
		this.duongDan = "";
	}

	String getNoiDung() {
		return noiDung;
	}

	void setNoiDung(String noiDung) {
		this.noiDung = noiDung;
	}

	String getDuongDan() {
		return duongDan;
	}

	void setDuongDan(String duongDan) {
		this.duongDan = duongDan;
	}

}
